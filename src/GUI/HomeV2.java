    private void registra_vaccinato_registra_vaccinato_btnMouseClicked(java.awt.event.MouseEvent evt) {
        String nome=registra_vaccinato_nome.getText();
        String cognome=registra_vaccinato_cognome.getText();
        String cod_fiscale=registra_vaccinato_codice_fiscale.getText();
        String comune_centro=registra_vaccinato_comune_centro.getText();
        String nome_centro=registra_vaccinato_centro_vaccinale.getText();
        String giorno=registra_vaccinato_giorno.getSelectedItem().toString();
        String mese=registra_vaccinato_mese.getSelectedItem().toString();
        String anno=registra_vaccinato_anno.getText();
        String tipo_vaccino=registra_vaccinato_tipologia.getSelectedItem().toString();
        String id_vacc=registra_vaccinato_idvax.getText();

        //controllo se i campi suono vuoti
        if(nome.isBlank()||cognome.isBlank()||cod_fiscale.isBlank()||nome_centro.isBlank()||
                giorno.isBlank()||mese.isBlank()||anno.isBlank()||tipo_vaccino.isBlank()||id_vacc.isBlank()) {
            //errore campi vuoti
            Message.warningMessage(this,"Compilare tutti i campi","Campi vuoti");
        }else{
            //controllo se il giorno è valido
            if(giorno.matches("[0-9]+") && !((giorno.length())>2) && Integer.parseInt(giorno)<32 && Integer.parseInt(giorno)>0){
                //controllo se anno è valido
                if(anno.matches("[0-9]+") && anno.length()==4 ){
                    //controllo se id vaccinazione è valido
                    if(id_vacc.matches("[0-9]+")) {
                        //istanzio oggetto data
                        DataLab data = new DataLab(giorno, mese, anno);
                        //istanzio oggetto vaccinato
                        Vaccinato vaccinatp_da_registrare = new Vaccinato(nome, cognome, cod_fiscale, nome_centro, comune_centro, data,
                                tipo_vaccino,Integer.parseInt(id_vacc));
                        try {
                            //scrivo sul socket
                            out.writeObject("REGISTRA VACCINATO");
                            out.writeObject(vaccinatp_da_registrare);
                        } catch (IOException e) {}
                        //apro JOptionPane per avvisare del corretto inserimento
                        Message.informationMessage(this,"Informazioni inserite con successo!","Successo");
                        //svuoto i campi
                        pulisci_campi();
                    }else{
                        //warnig id non valido
                        Message.warningMessage(this,"Per favore inserire un Id vaccinazione valido","id non valido");
                        registra_vaccinato_idvax.setText("");}
                }else{
                    //warnig anno non valido
                    Message.warningMessage(this,"Per favore inserire un anno valido","Anno non valido");
                    registra_vaccinato_anno.setText("");}
            }else {
                //warning giorno non valido
                Message.warningMessage(this,"Per favore inserire un giorno valido","Giorno non valido");
                registra_vaccinato_giorno.setSelectedIndex(0);}
        }
        pulisci_campi();
    }

